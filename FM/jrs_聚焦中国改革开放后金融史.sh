## **📚 学术型：深度分析金融改革**  
1️⃣ **《经济学家茶座》**  
   - 由知名经济学家和学者主持，探讨中国改革开放以来的货币政策、银行改革、资本市场发展等，货币政策的演变，
   - 适合对金融理论、政策演变感兴趣的听众。  
2️⃣ **《东吴相对论》**（部分金融专题）  
   - 结合哲学与经济学视角，分析中国金融市场的制度演变，比如国企改革、股市发展、货币体系等。解析金融演变，探讨市场经济的发展逻辑。
3️⃣ **《财新·天下财经》**  
   - 由财新传媒制作，深入剖析中国金融市场发展，涉及改革开放以来的重要金融政策和事件。  
4️⃣ **《中国经济纵横谈》**  
   - 关注中国经济发展，深入解读银行改革、证券市场发展、民间资本崛起等议题。  
5️⃣ **《商业就是这样》**（部分金融专题）  
   - 虽然是商业史播客，但有不少关于中国金融史的专题，比如80年代的银行改革、90年代的资本市场开放等，比如金融骗局、企业崛起与衰落等
4️⃣ 《经济学人·商业与金融》
   - 来自《经济学人》的官方播客，关注金融历史与全球经济格局的变化。经济趋势、市场动态等。
   

